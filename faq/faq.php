    </dl>
    <dl id="q3-2" class="fd_faq_inner">
      <dt>02.匿名組合契約とは何ですか？</dt>
      <dd>
        匿名組合契約は、商法535条に規定されている契約形態で、出資者（匿名組合員）が営業者の事業のために出資し、営業者がその事業より生ずる利益を出資者に分配することを約束する契約のことをいいます。<br>
        営業者はネクストシフト株式会社で、損失が発生した場合でも、匿名組合員が出資額を超えて損失の負担をすることはありません。
      </dd>
    </dl>
    <dl id="q3-3" class="fd_faq_inner">
      <dt>03.投資可能金額を０円にするための手続きを教えて下さい。</dt>
      <dd>
        マイページの入出金から、出金額を入力してください。<br>
        残高全額を入力ください。
      </dd>
    </dl>
    <dl id="q3-4" class="fd_faq_inner">
      <dt>04.ファンドが成立しない場合どうなりますか？</dt>
      <dd>最低成立金額に満たなかった場合、不成立となり、運用はされません。投資額をお客様の出資金を管理する口座に返却致します。</dd>
    </dl>
    <dl id="q3-5" class="fd_faq_inner">
      <dt>05.投資申込後のキャンセルは可能ですか？</dt>
      <dd>投資確定後のキャンセルはできません。また、運用開始後の途中解約は不可とさせていただいております。予めご了承ください。</dd>
    </dl>
    <dl id="q3-6" class="fd_faq_inner">
      <dt>06.最低投資金額はいくらですか？</dt>
      <dd>２万円となっています。</dd>
    </dl>
    <dl id="q3-7" class="fd_faq_inner">
      <dt>07.投資する手続きを教えて下さい。</dt>
      <dd>
        １．ファンド一覧からファンドを選び、投資予約金額を入力してください。<br>
        ２．契約締結前交付書面をご確認頂き、予約確認をしてください。<br>
        ３．マイページトップの投資予約一覧に行き、表示されている入金先口座番号にご入金ください。<br>
        ４．入金後、投資予約一覧で投資額を入力し、投資確定をします。
      </dd>
    </dl>
    <dl id="q3-8" class="fd_faq_inner">
      <dt>08.入出金の振込手数料について教えて下さい。</dt>
      <dd>
        入金時の振込手数料はお客様にご負担頂きます。振込手数料は金融機関によって異なりますので、ご利用の金融機関にご確認ください。<br>
        出金手数料は無料になります。
      </dd>
    </dl>
    <dl id="q3-9" class="fd_faq_inner">
      <dt>09.入金してからマイページに表示されるまでには、どれくらいの時間がかかりますか？</dt>
      <dd>
        通常、当日の9時から15時30分までに当社において着金を確認できたものについては確認から3時間以内にマイページ上に反映されます（15時30分以降の着金分は翌営業日の10時頃にマイページ上に反映されます。）。
      </dd>
    </dl>
    <dl id="q3-10" class="fd_faq_inner">
      <dt>10.出金申請してから登録銀行口座に振り込まれるまでにはどれくらいの時間がかかりますか？</dt>
      <dd>
        営業日の正午よりも前の出金申請につきましては原則として翌銀行営業日に、営業日の正午以降の出金申請につきましては原則として翌々銀行営業日後に、登録銀行口座への出金手続きを行います。
      </dd>
    </dl>
    <dl id="q3-11" class="fd_faq_inner">
      <dt>11.入金先金融機関名を教えて下さい。</dt>
      <dd>
        ジャパンネット銀行になります。なお入金先支店や口座番号はお客様によって異なりますので、マイページにある投資予約一覧のページでご確認をお願い致します。
      </dd>
    </dl>
