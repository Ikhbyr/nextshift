      <dt>12.スマートフォンからマイページにログイン後、投資予約金額を入力してPDF（締結前交付書面、匿名組合契約書、リスク確認および顧客表明事項）を開いても「同意します」にチェックが出来ません。</dt>
      <dd>
        スマートフォンでは、機種・ブラウザに関係なく複数のPDFが開かれた状態では「同意します」にチェックが出来ません。スマートフォン内で開いている全てのPDFを閉じてから投資予約の手続きをしてください。
      </dd>
    </dl>
    <dl id="q3-13" class="fd_faq_inner">
